// CÓDIGO NÃO EXECUTÁVEL

// Procedural
processamento(valor1, valor2, valor3)


// OO

obj = {
    valor1,
    valor2,
    valor3,
    processamento() {
        //...
    }
}

obj.processamento() // foco passou a ser o objeto

// Princípios importantes:
// 1. Abstração - conversão de objetos do mundo real para dentro de um software por meio de algoritmos simples, baseado no paradigma O.O.
// 2. Encapsulamento - detalhes de implementação escondidos, acessíveis por uma interface simples
// 3. Herança (prototype) - Compartilhamento de características ou partes, entre objetos, favorecer a composição 'é um'
// 4. polimorfismo - muitas formas de abstrair um objeto do mundo real